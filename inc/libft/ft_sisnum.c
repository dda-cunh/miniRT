#include "libft.h"

int	ft_sisnum(const char *s)
{
	if (s)
	{
		if (*s == '+' || *s == '-')
			s++;
		if (!*s)
			return (0);
		while (*s)
			if (!ft_isdigit(*s++))
				return (0);
		return (1);
	}
	return (0);
}
